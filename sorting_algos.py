#------------------------------------------------------------------------------ 
#        0. Bubble
#------------------------------------------------------------------------------
def bubble_sort(A):
    A = list(A) # make a copy so we don't sort the original
    while True:
        no_swaps = True
        for i in range(1, len(A)):
            if A[i-1] > A[i]:
                A[i-1], A[i] = A[i], A[i-1] # swap!
                no_swaps = False
        if no_swaps:
            break
    return A    # sorted in place

#------------------------------------------------------------------------------ 
#        1. Selection
#------------------------------------------------------------------------------
def selection_sort(A):
    A = list(A)
    sorted_A = []
    while A != []:
        # Find minimum element in A
        a_min = A[0]
        for a in A:
            if a < a_min:
                a_min = a
        sorted_A.append(a_min)
        A.remove(a_min)
    return sorted_A

